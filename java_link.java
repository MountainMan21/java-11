package java_concepts_revision;

public class java_link {

//       using static class variable outside the class
//	Constructor is always used when object of a class is created.
		
	private String name ;
	public static final String Best_Programmer = " first in java";
	


public java_link(String name) {
	super();
	this.name = name;
}




@Override
public String toString() {
	return "java_link [name=" + name + "]";
}




}



